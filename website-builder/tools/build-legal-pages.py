def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--project", required=True)
    ap.add_argument("--intake", required=True)
    args = ap.parse_args()

    project = Path(args.project).resolve()
    intake = json.loads(Path(args.intake).read_text())

    home_path = project / "src" / "app" / "page.tsx"
    home_text = home_path.read_text() if home_path.exists() else ""
    top, footer = extract_chrome(home_text)

    privacy_dir = project / "src" / "app" / "privacy"
    terms_dir = project / "src" / "app" / "terms"

    report = {"privacy_built": False, "terms_built": False, "skipped": []}

    if not (privacy_dir / "page.tsx").exists():
        privacy_dir.mkdir(parents=True, exist_ok=True)
        (privacy_dir / "page.tsx").write_text(build_privacy_jsx(intake, top, footer))
        report["privacy_built"] = True
    else:
        report["skipped"].append("privacy")

    if not (terms_dir / "page.tsx").exists():
        terms_dir.mkdir(parents=True, exist_ok=True)
        (terms_dir / "page.tsx").write_text(build_terms_jsx(intake, top, footer))
        report["terms_built"] = True
    else:
        report["skipped"].append("terms")

    print(json.dumps(report, indent=2))


if __name__ == "__main__":
    main()
