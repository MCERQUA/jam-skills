#!/usr/bin/env python3
"""
build-legal-pages.py — Generate /privacy and /terms pages from intake values.

Idempotent: skips pages that already exist. Generates minimal, accurate static
pages branded with the client's businessName, address, phone, email. Pages
inherit the site's chrome (navbar/footer) by reusing the home page's banner
+ navbar + footer markup so they look native to the site.

Usage:
  build-legal-pages.py --project <project-root> --intake <intake.json>
"""
import argparse
import json
import re
from datetime import date
from pathlib import Path


def extract_chrome(home_page_tsx: str) -> tuple[str, str]:
    """Extract banner+navbar (top chrome) and footer (bottom chrome) from the
    home page.tsx so legal pages can reuse them. Returns (top_chrome, footer)
    as JSX strings. If extraction fails, returns ('', '')."""
    # Top chrome: from start of return body up to and including the closing
    # </header> of the navbar.
    top_match = re.search(
        r'return\s*\(\s*\n?\s*<div[^>]*>\s*\n?(.*?</header>)',
        home_page_tsx,
        re.DOTALL,
    )
    top = top_match.group(1) if top_match else ""

    # Footer: <footer ...>...</footer>
    foot_match = re.search(r'(<footer[^>]*>.*?</footer>)', home_page_tsx, re.DOTALL)
    foot = foot_match.group(1) if foot_match else ""

    return top, foot


